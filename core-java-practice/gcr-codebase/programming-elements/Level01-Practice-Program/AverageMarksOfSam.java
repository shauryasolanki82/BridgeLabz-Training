public class AverageMarksOfSam {
    public static void main(String[] args) {
        int maths = 85; 
        int physics= 90; 
        int chemistry = 78; 
        int totalMarks = maths + physics + chemistry; 
        double averageMarks = totalMarks / 3.0; 
        System.out.println("Sam's average marks are: " + averageMarks);
    }
}
